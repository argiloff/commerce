import Grid from 'components/grid';
import ProductGridItems from 'components/layout/product-grid-items';
import { defaultSort, sorting } from 'lib/constants';
import { getProducts } from 'lib/woocommerce'; // WooCommerce statt Shopify

export const metadata = {
  title: 'Search',
  description: 'Search for products in the store.'
};

export default async function SearchPage(props: {
  searchParams?: Promise<{ [key: string]: string | string[] | undefined }>;
}) {
  const searchParams = await props.searchParams;
  const { sort, q: searchValue } = searchParams as { [key: string]: string };

  // Finde das Sortierkriterium aus der Liste oder nutze den Standard
  const { sortKey = 'NAME', reverse = false } =
    sorting.find((item) => item.slug === sort) || defaultSort;

  // Produkte von WooCommerce abrufen
  const products = await getProducts({
    searchQuery: searchValue,
    sort: sortKey,
    first: 12, // Begrenze die Anzahl der zurÃ¼ckgegebenen Produkte
  });

  const resultsText = products.length > 1 ? 'results' : 'result';

  return (
    <>
      {searchValue ? (
        <p className="mb-4">
          {products.length === 0
            ? 'There are no products that match '
            : `Showing ${products.length} ${resultsText} for `}
          <span className="font-bold">&quot;{searchValue}&quot;</span>
        </p>
      ) : null}
      {products.length > 0 ? (
        <Grid className="grid-cols-1 sm:grid-cols-2 lg:grid-cols-3">
          <ProductGridItems products={products} />
        </Grid>
      ) : null}
    </>
  );
}
